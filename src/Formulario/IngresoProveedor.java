    void cargar(String valor){
    String mostrar="SELECT * FROM proveedor WHERE CONCAT(cod_prov,ruc_prov,ciu_prov,nom_prov,nom_prod,dire_prov,tel_prov,rub_prov,email_prov) LIKE '%"+valor+"%'";
    String []titulos={"Codigo","RUC","Ciudad","Nom.Prov","Nom.Prod","Direccion","Telefon","Rubro","Email"};
    String []Registros=new String[9];
    model= new DefaultTableModel(null,titulos);
  
        try {
              Statement st = cn.createStatement();
              ResultSet rs = st.executeQuery(mostrar);
              while(rs.next())
              {
                  Registros[0]= rs.getString("cod_prov");
                  Registros[1]= rs.getString("ruc_prov");
                  Registros[2]= rs.getString("ciu_prov");
                  Registros[3]= rs.getString("nom_prov");
                  Registros[4]= rs.getString("nom_prod");
                  Registros[5]= rs.getString("dire_prov");
                  Registros[6]= rs.getString("tel_prov");
                  Registros[7]= rs.getString("rub_prov");
                  Registros[8]= rs.getString("email_prov");         
                  model.addRow(Registros);
              }
              tbclientes.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(IngresoProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    
  }
