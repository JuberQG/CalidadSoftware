private void guardar(){
       String cod,ruc,prov,prod,dire,tel,rub,ema,ciu;
            String sql="";
            cod=txtcod.getText();
            ruc=txtRuc.getText();
            ciu=txtCiudad.getText();
            prov=txtProveedor.getText();
            prod=txtProducto.getText();
            dire=txtDireccion.getText();
            tel=txtTelefono.getText();
            rub= txtRubro.getText();
            ema=txtEmail.getText();
            
            sql="INSERT INTO proveedor (cod_prov,ruc_prov,ciu_prov,nom_prov,nom_prod,dire_prov,tel_prov,rub_prov,email_prov) VALUES (?,?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement pst  = cn.prepareStatement(sql);
            pst.setString(1, cod);
            pst.setString(2, ruc);
            pst.setString(3, ciu);
            pst.setString(4, prov);
            pst.setString(5, prod);
            pst.setString(6, dire);
            pst.setString(7, tel);
            pst.setString(8, rub);
            pst.setString(9, ema);
            
            int n=pst.executeUpdate();
            if(n>0){
            JOptionPane.showMessageDialog(null, "Registro Guardado con Exito");
            bloquear();
            }
            cargar("");
        } catch (SQLException ex) {
            Logger.getLogger(IngresoProductos.class.getName()).log(Level.SEVERE, null, ex);
        }
}

